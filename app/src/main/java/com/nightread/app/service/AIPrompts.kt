package com.nightread.app.service

object AIPrompts {

    /**
     * Промпт для объяснения слова или термина в контексте предложения.
     */
    fun getWordExplanationPrompt(word: String, context: String): String {
        return """
            Ты — помощник по чтению. Твоя задача — объяснить значение слова или термина в контексте данного отрывка из книги.
            Слово: "$word"
            Контекст: "$context"
            
            Инструкции:
            1. Проанализируй, как это слово используется в предложении.
            2. Если это исторический факт, географическое название или культурная реалия, дай краткую справку.
            3. Если это обычное слово, дай толковое определение.
            4. Ответ должен быть кратким (1-2 предложения) и на русском языке.
            5. Если значение абсолютно непонятно, ответь: "Не удалось определить значение".
            
            Ответ:
        """.trimIndent()
    }

    /**
     * Промпт для генерации аннотации к книге на основе её фрагмента.
     */
    fun getBookAnnotationPrompt(title: String, contentSnippet: String): String {
        return """
            На основе следующего фрагмента книги "$title", напиши привлекательную и интригующую аннотацию.
            Фрагмент:
            $contentSnippet
            
            Инструкции:
            1. Опиши завязку сюжета и атмосферу.
            2. Не раскрывай спойлеры.
            3. Тон должен быть литературным и увлекательным.
            4. Ограничься 3-5 предложениями.
            
            Аннотация:
        """.trimIndent()
    }

    /**
     * Промпт для краткого содержания главы.
     */
    fun getChapterSummaryPrompt(chapterText: String): String {
        return """
            Прочитай текст главы и напиши её краткое содержание (summary).
            Текст главы:
            $chapterText
            
            Инструкции:
            1. Выдели ключевые события и изменения в состоянии героев.
            2. Ответ должен быть лаконичным (2-3 предложения).
            3. Избегай лишних деталей.
            
            Краткое содержание:
        """.trimIndent()
    }

    /**
     * Промпт для умного поиска по смыслу.
     */
    fun getSmartSearchPrompt(query: String, contentSnippet: String): String {
        return """
            Найди в тексте фрагмент, который наиболее точно соответствует смысловому запросу: "$query".
            Текст:
            $contentSnippet
            
            Инструкции:
            1. Ищи не только по точным словам, но и по смыслу.
            2. Если нашел, выведи цитату и кратко объясни, почему она подходит.
            3. Если ничего не подходит, ответь: "Совпадений не найдено".
            
            Результат:
        """.trimIndent()
    }

    /**
     * Промпт для анализа персонажей.
     */
    fun getCharacterAnalysisPrompt(contentSnippet: String): String {
        return """
            Проанализируй персонажей, упомянутых в этом фрагменте текста.
            Текст:
            $contentSnippet
            
            Инструкции:
            1. Перечисли имена главных персонажей.
            2. Для каждого дай краткую характеристику (черты характера, роль в сюжете).
            3. Формат: Имя - Описание.
            
            Анализ персонажей:
        """.trimIndent()
    }

    /**
     * Промпт для описания глав (интерактивное оглавление).
     */
    fun getChapterDescriptionPrompt(chapterIndex: Int, chapterText: String): String {
        return """
            Дай очень краткое описание того, что происходит в главе №$chapterIndex.
            Текст главы:
            $chapterText
            
            Инструкции:
            1. Одно короткое предложение.
            2. Только суть (например: "Герой прибывает в замок и встречает короля").
            
            Описание:
        """.trimIndent()
    }

    /**
     * Промпт для ответов на вопросы по книге.
     */
    fun getQuestionAnswerPrompt(question: String, bookContext: String): String {
        return """
            Ответь на вопрос пользователя, основываясь на содержании книги.
            Контекст из книги:
            $bookContext
            
            Вопрос: "$question"
            
            Инструкции:
            1. Используй только предоставленный контекст.
            2. Если ответа нет в тексте, честно скажи об этом.
            3. Ответ должен быть точным и вежливым.
            
            Ответ:
        """.trimIndent()
    }

    /**
     * Промпт для сравнения двух книг (или фрагментов).
     */
    fun getComparisonPrompt(textA: String, textB: String): String {
        return """
            Сравни два следующих текстовых фрагмента.
            Фрагмент 1: $textA
            Фрагмент 2: $textB
            
            Инструкции:
            1. Найди общие темы или стилистические сходства.
            2. Укажи на главные различия в настроении или событиях.
            3. Будь объективен.
            
            Сравнение:
        """.trimIndent()
    }

    /**
     * Промпт для генерации интеллектуальных тегов.
     */
    fun getTagsPrompt(contentSnippet: String): String {
        return """
            Проанализируй текст и предложи 5 наиболее подходящих тематических тегов (жанры, настроения, ключевые темы).
            Текст:
            $contentSnippet
            
            Формат: только слова через запятую.
            Теги:
        """.trimIndent()
    }

    /**
     * Промпт для рекомендаций на основе истории чтения.
     */
    fun getRecommendationsPrompt(history: String, availableBooks: String): String {
        return """
            На основе списка прочитанных книг, порекомендуй лучшие варианты из доступной библиотеки.
            История: $history
            Доступно: $availableBooks
            
            Инструкции:
            1. Выбери 3 книги.
            2. Объясни в одном предложении, почему каждая из них может понравиться.
            
            Рекомендации:
        """.trimIndent()
    }

    /**
     * Промпт для упрощения текста.
     */
    fun getSimplificationPrompt(text: String): String {
        return """
            Перескажи этот сложный или архаичный фрагмент текста простыми современными словами, сохраняя смысл.
            Текст:
            $text
            
            Упрощенный текст:
        """.trimIndent()
    }

    /**
     * Промпт для поиска цитат по теме.
     */
    fun getQuoteSearchPrompt(topic: String, contentSnippet: String): String {
        return """
            Найди в тексте самую яркую или мудрую цитату на тему: "$topic".
            Текст:
            $contentSnippet
            
            Цитата:
        """.trimIndent()
    }
}
