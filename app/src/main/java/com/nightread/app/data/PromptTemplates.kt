package com.nightread.app.data

object PromptTemplates {

    const val SYSTEM_PROMPT = """Ты — литературовед-аналитик, интегрированный в приложение для чтения книг. Твоя задача — помогать пользователю разбираться в тексте.

Правила:
1. Отвечай строго на русском языке.
2. Если не знаешь ответа или в контексте нет информации — скажи прямо: "В контексте книги нет информации по данному вопросу. Попробуйте уточнить запрос или выберите другого героя."
3. Не выдумывай цитаты. Используй только те отрывки, которые переданы в запросе.
4. Структурируй ответ: используй заголовки и маркированные списки для удобства чтения.
5. Твой тон — нейтральный, экспертный, без эмоций и оценок вроде "это великая книга".
6. Не пиши "Анализ:" или "Ответ:" в начале. Начинай сразу с содержания.
7. Всегда объединяй падежи одного имени (Эдвин, Эдвина, Эдвину — один персонаж).
8. Если в контексте нет упоминания запрашиваемого персонажа — честно ответь, что информации в доступных отрывках нет."""

    // 1. Краткое содержание (3-5 предложений: тема, герой, конфликт, развязка)
    fun bookSummary(text: String, bookTitle: String = ""): String =
        """На основе контекста составь краткое содержание книги "${if (bookTitle.isNotBlank()) bookTitle else "произведения"}" в 3–5 предложениях.
Контекст: $text

Укажи: основную тему, главного героя, ключевой конфликт и основную развязку (если она известна). Не углубляйся в детали."""

    // 2. Кто это? (краткая справка о персонаже)
    fun whoIsCharacter(characterName: String, text: String): String =
        """Кто такой $characterName в этой книге?
Контекст из книги: $text

Дай краткую справку: роль в сюжете, основные черты характера, связь с другими героями. Начинай сразу с содержания."""

    // 3. Глубокий анализ персонажа (5 пунктов: Мотивация, Динамика, Конфликты, Влияние, Авторская идея)
    fun characterAnalysis(characterName: String, bookTitle: String, text: String): String =
        """Проанализируй персонажа $characterName из книги "${if (bookTitle.isNotBlank()) bookTitle else "произведения"}".
Контекст из книги: $text

Дай анализ строго по структуре:
1. Мотивация и внутренние противоречия.
2. Динамика характера по ходу сюжета.
3. Внутренние и внешние конфликты.
4. Влияние на поступки других героев.
5. Авторская идея, которую воплощает этот персонаж.

Не пересказывай сюжет. Давай интерпретацию."""

    // 4. Пояснение слова (Прямое значение, Смысл в контексте, Устаревшее/диалектное)
    fun explainTerm(wordOrPhrase: String, text: String): String =
        """Объясни значение слова "$wordOrPhrase" в данном контексте.
Контекст: $text

Дай: 1) Прямое значение, 2) Смысл в этом отрывке, 3) Если слово устаревшее или диалектное — укажи это."""

    // 5. Перевод слова (Иностранный -> Русский, Русский -> Английский)
    fun translateWord(word: String, contextSnippet: String = ""): String =
        """Переведи слово или выражение "$word".
Если слово на иностранном языке — переведи на русский.
Если слово на русском — переведи на английский.
${if (contextSnippet.isNotBlank()) "Контекст употребления: $contextSnippet" else ""}

Дай только перевод и краткое пояснение при необходимости. Начинай сразу с перевода."""

    // 6. Сравнительный анализ двух персонажей
    fun compareCharacters(name1: String, name2: String, bookTitle: String, text: String): String =
        """Сравни персонажей $name1 и $name2 из книги "${if (bookTitle.isNotBlank()) bookTitle else "произведения"}".
Контекст из книги: $text

Сравни по пунктам:
1. Общие черты.
2. Различия в мотивации.
3. Различия в динамике.
4. Как их конфликт или союз влияет на сюжет.
5. Авторская идея в каждом."""

    // 7. Анализ сюжетной арки персонажа
    fun characterArcAnalysis(characterName: String, bookTitle: String, text: String): String =
        """Проанализируй сюжетную арку персонажа $characterName из книги "${if (bookTitle.isNotBlank()) bookTitle else "произведения"}".
Контекст из книги: $text

Опиши:
1. Экспозицию — появление в сюжете.
2. Развитие конфликта.
3. Кульминацию.
4. Развязку.

Покажи, как каждый этап влияет на персонажа. Не пересказывай сюжет — анализируй."""

    // 8. Анализ всех персонажей книги
    fun analyzeAllCharacters(bookTitle: String, text: String): String =
        """Проанализируй всех персонажей книги "${if (bookTitle.isNotBlank()) bookTitle else "произведения"}".
Контекст из книги: $text

Объединяй все падежи одного имени в одного персонажа.

Для каждого персонажа дай:
1. Мотивация и внутренние противоречия.
2. Динамика характера по ходу сюжета.
3. Внутренние и внешние конфликты.
4. Влияние на поступки других героев.
5. Авторская идея, которую воплощает этот персонаж.

Не пересказывай сюжет. Давай интерпретацию."""
}
