        if (title.isBlank()) return null
        val cleanTitle = title.lowercase(Locale.ROOT)
            .replace(Regex("[^a-zA-Zа-яА-ЯёЁ0-9\\s]"), "")
            .trim()
        
        for ((key, data) in CLASSICS_DATABASE) {
            val cleanKey = key.lowercase(Locale.ROOT)
                .replace(Regex("[^a-zA-Zа-яА-ЯёЁ0-9\\s]"), "")
                .trim()
